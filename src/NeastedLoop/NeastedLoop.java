package NeastedLoop;

public class NeastedLoop {

	public static void main(String[] args) {
		for(int i=1; i<4; i++) {
			for(int j=0; j<=2; j++) {
				System.out.println("hello ");
				break;
		}
			System.out.println("bye");
	}
		
		boolean day=true;
		for(int i=1; i<=3; i++) {
			while(day) {
				System.out.println("good day ");
				break;
			}
			System.out.println(i);
			
		}
		

}}
